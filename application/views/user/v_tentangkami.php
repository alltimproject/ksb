  <h2 class="text-info">Sejarah Singkat</h2>

  <p style="text-align:justify; font-size:15px; margin-top:50px; margin-bottom:40px;">

    Kampung Siaga Bencana (KSB) Kelurahan Penjaringan diresmikan pada tanggal 17 November 2015 oleh Dinas Sosial DKI Jakarta yang mengacu dalam Peraturan Menteri Nomor 128 tahun 2011 disebutkan KSB merupakan wadah yang berbasis masyarakat. Wadah dalam konteks ini dapat dipahami sebagai lembaga atau organisasi. Meskipun KSB tersebut dibentuk atau diinisiasi oleh pemerintah, namun esensi ini memberikan peluang bagi masyarakat untuk menentukan kedudukan lembaga sesuai dengan aspirasi masyarakat yang hendak dibentuk. Dalam Petunjuk teknis Kampung Siaga Bencana (Direktorat Perlindungan Sosial Korban Bencana Alam Kementerian Sosial RI, 2012, hal.33) dijelaskan bahwa Kedudukan KSB berada ditingkat Kecamatan, Kelurahan, Desa, Dusun dalam wilayah hukum Negara Kesatuan Republik Indonesia yang telah ditentukan secara bersama pada saat pembentukan keanggotaan Tim KSB. Kampung Siaga Bencana (KSB) telah dijadikan sebagai salah satu basis kekuatan dan mempunyai posisi yang strategis dalam penanggulangan bencana alam.
  </p>

